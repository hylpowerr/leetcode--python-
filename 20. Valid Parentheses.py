"""
Given a string containing just the characters '(', ')', '{', '}', '[' and ']', determine if the input string is valid.

The brackets must close in the correct order, "()" and "()[]{}" are all valid but "(]" and "([)]" are not.
"""
class Solution(object):
    def __init__(self):
        self.left_pa_map = {}
        self.left_pa_map['('] = 0
        self.left_pa_map['['] = 1
        self.left_pa_map['{'] = 2

        self.right_pa_map = {}
        self.right_pa_map[')'] = 0
        self.right_pa_map[']'] = 1
        self.right_pa_map['}'] = 2
    def isValid(self, s):
        """
        :type s: str
        :rtype: bool
        """
        stack =[]


        for i in range(len(s)+1):
            if i > len(s)-1:
                if len(stack) == 0:
                    return True
                else:
                    return False
            ch = s[i]
            if ch in self.left_pa_map:
                stack.append(self.left_pa_map[ch])
                continue
            elif ch in self.right_pa_map:
                if len(stack)>0:
                    if stack[-1] == self.right_pa_map[ch]:
                        stack = stack[:-1]
                        continue
                    else:
                        return False
                else:
                    return False


